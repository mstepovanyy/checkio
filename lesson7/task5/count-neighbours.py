def count_neighbours(grid, row_index, col_index):
    
    selected_item = grid[row_index][col_index]
    
    result = 0
    
    for r_index, row in enumerate(grid):
        if r_index in (row_index -1, row_index, row_index + 1):
            for c_index, col in enumerate(row):
                if c_index in (col_index - 1, col_index, col_index + 1):
                    result += col
                

    return result - selected_item


