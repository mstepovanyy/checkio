def move_to(maze_map, current, letter):
    position = add_l(current, move[letter])
    
    if position[0] == 10 and position[1] == 10:
        return letter
    path = None
    maze_map[position[0]][position[1]] = 1
    for item in get_possible_ways(maze_map, position):
        path = move_to(copy.copy(maze_map), position, item)
        if path:
            return letter + path
    return path

def get_possible_ways(maze_map, position):
    result = []
    for letter, pos in move.items():
        next = add_l(position, pos)
        if maze_map[next[0]][next[1]] == 0:
            result.append(letter)
    return result

def add_l(first, second):
    return (first[0] + second[0], first[1] + second[1])

